    local castname= spell
    local icon
	if zUI.loc =="zhCN" and zSpellNamecntoen[spell] then
       castname =  zSpellNamecntoen[spell]
    

    end
    if spellIcons[castname] then
         icon = spellIcons[castname] 
    end
	
	if not icon then
		return nil
	end
	return "Interface\\Icons\\" .. icon
end
